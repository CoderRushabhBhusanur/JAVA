class DemoVariable{
public static void main(String[] args){
int rollNumber = 10;
System.out.println(rollNumber);

float marks = 99.98f;
System.out.println(marks);

char ch = 'Y';
System.out.println(ch);

boolean result=true;
System.out.println(result);

long l = 10;
int a = 20;
a =(int) l; //narrowing
l = a; //widening
System.out.println(a);
System.out.println(l);



}
}